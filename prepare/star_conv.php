for($i=0;$i<sizeof($stars);$i++) {
	$ra = $stars[$i]['ra'];
	$dec = $stars[$i]['dec'];
	$m = $stars[$i]['mag'];
	if (!empty($ra)) {
		$a = $ra * 360 / 24;
		$x = $r * cos(deg2rad($a)) * cos(deg2rad($dec));
		$z = $r * sin(deg2rad($a)) * cos(deg2rad($dec));
		$y = $r * sin(deg2rad($dec));
		array_push($star_cors, array('x' =>  $x, 'y' =>  $y, 'z' =>  $z, 'm' => $m));
